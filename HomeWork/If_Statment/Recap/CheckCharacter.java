package If_Statment.Recap;

public class CheckCharacter {
    public static void main(String[] args) {
        char litter = 'S';
        if (litter > 'a' && litter <= 'z') {
            System.out.println("lowercase");

        }
        if (litter > 'A' && litter <= 'Z') {
            System.out.println("Uppercase");


        }
    }
}
